#include <iostream>  // Includes the input-output stream library
using namespace std; // Allows use of standard names like cout without needing std:: prefix

int main() {
    // Step 1: Declare and initialize the first integer variable with value 8
    int firstNumber = 8;

    // Step 2: Declare and initialize the second integer variable with value 10
    int secondNumber = 10;

    // Step 3: Declare a third integer variable that stores the sum of the two previous variables
    int sum = firstNumber + secondNumber;

    // Step 4: Print the result (sum) to the console
    cout << "The sum is: " << sum << endl;

    return 0; //  End the program successfully 
}
